/** 섹션 제목. 글 본문 안의 H2/H3 헤딩으로 렌더링됩니다. */
export type HeadingBlock = {
  type: "h2" | "h3";
  text: string;
};

/**
 * 본문 단락.
 * text 안에서 다음 인라인 마크업을 지원합니다:
 *   **굵게**   →  <strong>굵게</strong>
 *   _기울임_   →  <em>기울임</em>
 */
export type ParagraphBlock = {
  type: "p";
  text: string;
};

/** 순서 없는(unordered) 또는 순서 있는(ordered) 리스트. */
export type ListBlock = {
  type: "list";
  ordered?: boolean;
  items: string[]; // 각 항목도 인라인 마크업 지원
};

/**
 * 비교표.
 * headers: 첫 번째 행(헤더 셀들)
 * rows: 본문 행들. 각 행은 셀 문자열 배열.
 * caption: 표 아래 설명 (선택)
 */
export type TableBlock = {
  type: "table";
  headers: string[];
  rows: string[][];
  caption?: string;
};

/** 강조 박스: 팁/주의/요약 등에 사용. */
export type CalloutBlock = {
  type: "callout";
  variant?: "info" | "warn" | "tip" | "summary";
  title?: string;
  text: string;
};

export type Block =
    | HeadingBlock
    | ParagraphBlock
    | ListBlock
    | TableBlock
    | CalloutBlock;

// ─────────────────────────────────────────────────────────────
// 블로그 글 타입
// ─────────────────────────────────────────────────────────────

export type BlogPost = {
  slug: string;
  category: string;
  title: string;
  description: string;
  date: string;
  /**
   * 글 본문.
   * - Block[] (권장): 헤딩/표/리스트 등 풍부한 마크업
   * - string[] (legacy): 단순 단락 배열. 자동으로 p 블록으로 변환됨.
   */
  content: Block[] | string[];
  /** 사이트에 노출 여부. 기본값 true. false면 목록·sitemap·상세에서 모두 제외됩니다. */
  published?: boolean;
};

/**
 * legacy string[] 형식을 Block[]로 정규화합니다.
 * 렌더러에서 호출되는 헬퍼.
 */
export function normalizeContent(content: Block[] | string[]): Block[] {
  if (content.length === 0) return [];
  if (typeof content[0] === "string") {
    return (content as string[]).map((text) => ({ type: "p", text }));
  }
  return content as Block[];
}

